//equality operators
var x = 1
console.log(x == '1') //lose comparison, compare the value, not type
console.log(x === '1') //strict comparison, not only checking the value, we also checking the data type of the value
//1 - is a number,
//'1' - is a string
console.log(x === 1) //gives true